using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public enum UIType
{
    /// <summary>
    /// 固定类型，一般作为背景
    /// </summary>
    Fixed,
    /// <summary>
    /// 普通窗口，一般窗口都用这个
    /// </summary>
    Normal,
    /// <summary>
    /// 弹窗，可以在某些窗口上弹出的窗口
    /// </summary>
    PopUp
}
